package com.yu.webstudy.controllerAdvice;

public class User {
	private String name;
	private Integer age;
	
	public final String getName() {
		return name;
	}
	public final void setName(String name) {
		this.name = name;
	}
	public final Integer getAge() {
		return age;
	}
	public final void setAge(Integer age) {
		this.age = age;
	}
}
